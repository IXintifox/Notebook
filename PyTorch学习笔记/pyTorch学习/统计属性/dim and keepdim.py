import torch

a = torch.randn(2, 3, 10)

print(a.shape)
print(a)
print(a.max())  #全局最大值，意义不大
print(a.max(dim=0))    #不仅会输出位置，还会输出数值，argmax只输出数值
print(a.max(dim=1))
print(a.max(dim=2))

#keepdim会保持原来的维度
print('原有的数值：\n', a.max(dim=2))
print('改变的数值：\n', a.max(dim=2, keepdim=True))          #仅有行比较才有意义，最终得出每行的最大值。

